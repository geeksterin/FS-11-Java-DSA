package Day07;
public class _00Character {
    public static void main(String[] args) {
        char ch1 = 'A';
        int val1 = ch1;
        System.out.println(ch1 + " -> " + val1);

        char ch7 = (char) val1;
        System.out.println(ch7);

        char ch2 = ' ';
        int val2 = ch2;
        System.out.println(ch2 + " -> " + val2);

        char ch3 = '@';
        int val3 = ch3;
        System.out.println(ch3 + " -> " + val3);

        char ch4 = '\t';
        int val4 = ch4;
        System.out.println(ch4 + " -> " + val4);

        char ch5 = '5';
        int val5 = ch5;
        System.out.println(ch5 + " -> " + val5);

        char ch8 = (char) val5;
        System.out.println(ch8);

        char ch6 = '\u0958';
        int val6 = ch6;
        System.out.println(ch6 + " -> " + val6);

        
    }
}